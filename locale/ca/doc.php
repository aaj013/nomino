<p>Nomino és un editor especialitzat de l'OpenStreetMap, indicat per a traduir noms dels llocs.</p>
<p>Al contrari que el JOSM i altres, Nomino no és dissenyat per editar-ho tot a l'OpenStreetMap.
	En comptes d'això, proporicona una interfície simple, especialment dissenyada per als nous col·laboradors.</p>
<h2>Cerca de llocs</h2>
<p>Nomino no baixa una àrea sencera de l'OMS, però podeu cercar per un lloc per editar-lo.</p>
<p>Només cal que escriviu un nom de lloc o una adreça a cercar, i aleshores feu clic a «Cerca», 
	es proposen diferents objectes de l'OSM en el llistat de la dreta i podeu veure-les al mapa.
	Fent clic en un nom de lloc, podeu editar l'objecte.</p>
<p>Si preferiu seleccionar llocs al mapa, podeu fer clic amb el botó secundari en algun punt del mapa.
	El punt que heu clicat es troba en una ciutat, comarca, província, estat...
	Aquestes entitats es mostren en un llistat, trieu un d'aquests elements per editar la ciutat, l'estat...</p>
<p> Donat el seu número identificador, podeu obrir un objecte de l'OSM.
 Feu clic al botó «Objecte de l'OSM», seleccioneu el tipus d'objecte i el número d'objecte i, aleshores, feu clic a «Cerca» per obrir-lo.</p>
<h2>Traducció</h2>
<h2>Canvis</h2>
<p>Tots els objectes canviats es mostre a la pestanya «Canvis», juntament amb les etiquetes que heu editat. Feu clic al nom del lloc per obrir-lo altra vegada,
	o feu clic al botó «Desfés» per descartar els canvis.</p>
<p>Per desar els canvis a la base de dades de l'OpenStreetMap, feu clic «Envia els canvis». Es requereix un compte d'usuari de l'OpenStreetMap, i haureu d'autoritzar el Nomino per editar dades de l'OSM.</p>
<p>El botó «Baixa el fitxer OSM» retorna un fitxer XML de l'OpenStreetMap, que podeu obrir amb el JOSM o Merkaartor..</p>
<h2>Preferències d'usuari</h2>
<p>El quadre de preferències és útil per seleccionar la capa de mapa que voleu usar a la pestanya «Cerca llocs».</p>
<p>Molts usuaris volen traduïr noms de lloc a la seva pròpia llengua. L'opció «Llengua preferida» defineix la llengua que es mostrarà sempre a la pestanya «Traducció»; si no existeix traducció en aquesta llengua, s'afegirà una línia nova buida.</p>
